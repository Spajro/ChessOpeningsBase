package gui.config;

import java.awt.*;

public class BoardDisplayConfiguration {
    private Color backgroundColor = Color.WHITE;
    private Color whiteFieldColor = Color.WHITE;
    private Color blackFieldColor = Color.DARK_GRAY;

    public Color getBackgroundColor() {
        return backgroundColor;
    }

    public Color getWhiteFieldColor() {
        return whiteFieldColor;
    }

    public Color getBlackFieldColor() {
        return blackFieldColor;
    }
}
